console.log(5 == "5");            //true      o 5 realmente é igual a 5.
console.log(5 === "5");           //false     o 5 até é 5, mas seu tipo é diferente.
console.log(0 == false);          //true      o 0 é a mesma coisa que false por não ser nada.
console.log(0 === false);          //false    eles são de tipos diferentes.
console.log(null == undefined);    //true     ambos são sem valor.
console.log(null === undefined);   //false    eles são de tipos diferentes.