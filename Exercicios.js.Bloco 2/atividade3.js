const dia = "5";
let diaDaSemana;

switch (dia) {
    case "1":
        diaDaSemana = "Domingo";
        break
    case "2":
        diaDaSemana = "Segunda";
        break
    case "3":
        diaDaSemana = "Terça-Feira";
        break
    case "4":
        diaDaSemana = "Quarta-Feira";
        break
    case "5":
        diaDaSemana = "Quinta-Feira";
        break
    case "6":
        diaDaSemana = "Sexta-Feira";
        break
    case "7":
        diaDaSemana = "Sábado";
        break
      default:
        diaDaSemana = "Dia da Semana não encontrado";

}

console.log(diaDaSemana);
    